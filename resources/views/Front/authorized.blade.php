                <h2 class="title">GInI Authorized Entities</h2>
<p>In order to carry out its world-class certification and accreditation program around the world, the Global Innovation Institute partners with two specific types of authorized entities, namely Authorized Innovation Providers® (AInPs®) and Authorized Innovation Assessors® (AInAs®).</p>

<p>Authorized Innovation Providers® are independently owned and managed businesses – often training and/or consulting businesses – who have chosen to partner with GInI, and thereby operate under GInI’s guidance, to deliver GInI’s certification and accreditation program to their respective clients.  Accordingly, whenever a business wishes to receive exam-preparation training, have a GInI Innovation Maturity Assessment conducted, or apply for any of the accreditations in GInI’s Accreditation Program, they must work through a GInI AInP® to do so.  GInI does not offer these services directly; they are offered exclusively through GInI’s AInP® network around the world.</p>

<p>To learn more about becoming an Authorized Innovation Provider®, visit GInI’s AInP® page.</p>

<p>Authorized Innovation Assessors® are independent professionals who have:  a) met certain strict qualifications;  b) completed GInI’s AInA® training program; and  c) passed GInI’s AInA® authorization exam – thus demonstrating their understanding of, and their ability to carry out, both GInI’s CInOrg Innovation Maturity Assessments and GInI’s Innovation Accreditation Program.  The bulk of the actual work, when it comes to assessments and endorsement applications, thus falls to the GInI AInA®.  Moreover, in conducting this work, AInAs® must work directly under the oversight and supervision of a GInI AInP®, so as to ensure complete transparency and accountability in all work undertaken.</p>

<p>To learn more about becoming an Authorized Innovation Assessor®, visit GInI’s AInA® page.</p>

