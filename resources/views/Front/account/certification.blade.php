                <h2 class="title">GInI Certification Program</h2>
<p>A core element of the Global Innovation Institute’s mission is the operation of the world’s foremost certification program for business innovation.  Accordingly, the Institute has developed and operates the world’s most comprehensive and professionally-managed evidence-based innovation certification program for both individuals and organizations.</p>

<p>At the center of this is GInI’s professional credentialing examination development process.  This process has sought to align with all certification industry best practices, such as those found in the Standards for Educational and Psychological Testing.   As a result, GInI’s processes – and the certifications that results from them – stand head and shoulders above any other credentialing program anywhere in the world in the field of business innovation.</p>

<p>GInI’s certifications for professionals are – in keeping with industry best practices – all based on the use of a Role Delineation Study (RDS).  This is a leading best practice used by organizations working to offer valid and reliable professional certifications, as it ensures the validity of the certifying exam and thus gives assurance that the exam properly assesses all of the knowledge and skills needed to carry out that role in the real world.  GInI credential holders can be confident therefore that their professional credentials have been developed in accordance with industry best practices and are based on the inputs of the practitioners who have established these standards.</p>

<p>The GInI Professional Certification program awards four different certifications to individuals, each based on the passing of a professional examination.  These four certifications are:</p>
<ul>
	<li>1. CInP® – Certified Innovation Professional®</li>
	<li>2. CDTP® – Certified Design Thinking Professional®</li>
	<li>3. CInS® – Certified Innovation Strategist®
</li>
	<li>4. CCInO® – Certified Chief Innovation Officer® 
</li>
</ul>
<p>GInI’s certification for organizations – the Certified Innovative Organization® (CInOrg®) – is based on the use of the GInI InMAT – one of the world’s most comprehensive and thorough Innovation Maturity Assessment Tools.  This tool was developed through years of research into corporate innovation programs and strategies, and included a comprehensive benchmarking of all other innovation maturity assessments in use around the world.  The result is the world’s most well thought-out Innovation Maturity Assessment tool, assessing as it does the three core elements that make up a mature innovative organization – its Innovation Strategy, its Innovation Program Architecture, and its Innovation Outcomes & Results.  To ensure objectivity, this assessment must be conducted by at least two (2) independent Authorized Innovation Assessors® (AInAs®) – professionals who have been trained and authorized by GInI for this task.  The result is the world’s most reliable and accurate innovation maturity assessment process, giving those organizations who achieve this certification confidence in its findings.</p>

